public class Woman {
    private int age;
    private float weight;

    public Woman(int age, float weight){
        this.age = age;
        this.weight = weight;
    }
    public Woman(){
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }
}
